-- Main tangling / code generation (replaces the huge 'all' target)
newaction {
   trigger     = "all",
   description = "Generate all code, scripts and styles (normal build)",
   execute     = function ()
      local tools  = LIB3 .. P .. "tools"
      local lua_lib = LIB3 .. P .. (LUAVERSION == "lua30" and "lua-3.0" or "lua-2.5")

      local function tangle(dir, extra_opts)
         local opts = extra_opts or ""
         local cmd = string.format('cd %s && make NOTANGLE=more TANGLEOPTS="%s" OUTPUT=%s%s%s TOOLS=%s LUA=..%slua-2.5 LUALIB=%s',
                                   dir, opts, LIB3, P, dir, tools, P, lua_lib)
         print("==> " .. cmd)
         os.execute(cmd)
      end

      -- Ensure lib directories exist
      os.mkdir(path.join(LIB3, "c"))
      os.mkdir(path.join(LIB3, "lua"))
      os.mkdir(path.join(LIB3, "tex"))

      tangle("c",   '| nocond lua2 | nocond | ' .. tools .. P .. 'markup | ' .. tools .. P .. 'nt')
      tangle("lua", '| nocond lua2 | ' .. tools .. P .. 'markup | ' .. tools .. P .. 'nt')
      tangle("tex", '')
   end
}

newaction {
   trigger     = "install",
   description = "Install binaries and TeX styles",
   execute     = function ()
      print("Installing binaries to " .. BINDIR)
      os.copyfile(path.join(LIB3, "c", "no", E), -- E := extension?
                  BINDIR, "no", E) -- E := extension?
      os.copyfile(LIB3, "c", "nwmtime", E, -- E := extension?
                  BINDIR, "nwmtime", E) -- E := extension?

      print("Installing scripts")
      os.copydir(path.join(LIB3, "lua"), BINDIR, "*.lua")

      print("Installing TeX styles")
      local texdest = path.join(TEXINPUTS, "latex", "misc")
      os.mkdir(texdest)
      os.copyfile(path.join(LIB3, "tex", "noweb.sty"),
                  path.join(texdest, "noweb.sty"))
      os.copyfile(path.join(LIB3, "tex", "nwmac.tex"),
                  path.join(TEXINPUTS, "generic", "misc", "nwmac.tex"))
   end
}

