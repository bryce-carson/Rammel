   local function ask(question, default)
      io.write(question .. " [" .. default .. "]: ")
      io.flush()
      local answer = io.read():match("^%s*(.-)%s*$")
      if answer == "" then return default end
      return answer
   end

   local platform = os.host()
   if platform == "windows" then
      BINDIR    = ask("Install binaries to", "C:\\Rammel\\bin")
      TEXINPUTS = ask("TeX inputs base directory", "C:\\texlive\\texmf-local")
      LIB3      = ask("Rammel private library directory", "C:\\Rammel\\lib")
   else
      BINDIR    = ask("Install binaries to", "/usr/local/bin")
      TEXINPUTS = ask("TeX inputs base directory", "/usr/local/share/texmf/tex/latex/local")
      LIB3      = ask("Rammel private library directory", "/usr/local/lib/rammel")
   end

   local luv = ask("Lua version for normal build (lua25 / lua30)", "lua25")
   if luv == "lua30" then LUAVERSION = "lua30" else LUAVERSION = "lua25" end

   print("\nConfiguration summary:")
   print("   Binaries      -> " .. BINDIR)
   print("   TeX inputs    -> " .. TEXINPUTS)
   print("   Library dir   -> " .. LIB3)
   print("   Lua version   -> " .. LUAVERSION:upper())
   print("")
end

